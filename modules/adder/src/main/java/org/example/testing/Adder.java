package org.example.testing;

/**
 * Provides an {@link #add(int, int)} method for integers
 */
public class Adder {
    /**
     * Adds two integers
     *
     * @param a the first summand
     * @param b The second summand
     * @return the added integer
     */
    public int add(int a, int b) {
        return a + b;
    }
}