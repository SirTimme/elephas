package org.example;

/**
 * Provides a {@link #subtract(int, int)} method for integers
 */
public class Subtracter {
    /**
     * Subtracts two integers
     *
     * @param a the minuend
     * @param b the subtrahend
     * @return The subtracted integer
     */
    public int subtract(int a, int b) {
        return a - b;
    }
}